## Numbers
# Example:
puts "5 + 5 = #{5 + 5}"
# This is a string interpolation
# It will print 5 + 5

puts 4 + 2.5   # 6.5
puts 100 - 20  # 80
puts 20 / 5    # 4
puts 13 % 3    # 1

puts 4 == 4       # true
puts 5 + 3 != 7   # true
puts 6 > 0        # true
puts 6 < 0        # false
puts 25 <= 25     # true
puts 12 >= 16     # false
